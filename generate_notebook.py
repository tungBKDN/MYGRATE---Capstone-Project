            "    if not root_pom.exists():\n",
            "        print(f\"No root pom.xml found at {root_pom}\")\n",
            "        return []\n",
            "        \n",
            "    project = MavenProject(str(root_pom))\n",
            "    editor = project.get_pom_editor()\n",
            "    \n",
            "    for idx, action in enumerate(actions, start=1):\n",
            "        act_type = action.get(\"action\")\n",
            "        gid = action.get(\"group_id\")\n",
            "        aid = action.get(\"artifact_id\")\n",
            "        version = action.get(\"version\")\n",
            "        prop_name = action.get(\"property_name\")\n",
            "        executions = action.get(\"executions\")\n",
            "        configuration = action.get(\"configuration\")\n",
            "        \n",
            "        # Coordinate Validation & Correction to avoid LLM hallucinations (e.g. org.sonarsource.api:sonar-plugin-api:5.4.0)\n",
            "        if act_type in (\"update_dependency\", \"add_dependency\") and gid and aid:\n",
            "            matched_key = None\n",
            "            for key in selected_sol:\n",
            "                if key.endswith(f\":{aid}\"):\n",
            "                    matched_key = key\n",
            "                    break\n",
            "            if matched_key:\n",
            "                target_gid, target_aid = matched_key.split(\":\")\n",
            "                target_version = selected_sol[matched_key]\n",
            "                if gid != target_gid or version != target_version:\n",
            "                    print(f\"  [VALIDATOR] Correcting dependency: {gid}:{aid}:{version} -> {target_gid}:{target_aid}:{target_version}\")\n",
            "                    gid = target_gid\n",
            "                    version = target_version\n",
            "        \n",
            "        status = \"ok\"\n",
            "        try:\n",
            "            if act_type == \"add_dependency\":\n",
            "                editor.add_dependency(gid, aid, version)\n",
            "            elif act_type == \"update_dependency\":\n",
            "                if editor.dependency_exists(gid, aid):\n",
            "                    def update_func(dep_elem):\n",
            "                        version_elem = editor.ensure_element(dep_elem, \"m:version\")\n",
            "                        version_elem.text = version\n",
            "                    editor.update_dependency(gid, aid, update_func)\n",
            "                else:\n",
            "                    editor.add_dependency(gid, aid, version)\n",
            "            elif act_type == \"ensure_property\":\n",
            "                editor.ensure_property(prop_name, version)\n",
            "            elif act_type == \"add_plugin\":\n",
            "                editor.add_plugin(\n",
            "                    group_id=gid,\n",
            "                    artifact_id=aid,\n",
            "                    version=version,\n",
            "                    configuration=configuration,\n",
            "                    executions=executions\n",
            "                )\n",
            "            else:\n",
            "                status = \"error\"\n",
            "        except Exception as e:\n",
            "            status = \"error\"\n",
            "            print(f\"  [ERROR] Action {act_type} failed: {e}\")\n",
            "            \n",
            "        detail = prop_name or f\"{gid}:{aid}\"\n",
            "        print(f\"  [{idx}] Action: {act_type}, target: {detail} -> status: {status}\")\n",
            "        results.append((action, status))\n",
            "    return results\n",
            "\n",
