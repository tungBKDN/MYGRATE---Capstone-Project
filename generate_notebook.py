            "def execute_pom_migration_actions(actions, project_path, selected_sol):\n",
            "    \"\"\"\n",
            "    Executes the generated actions on the pom.xml of the project.\n",
            "    Validates and corrects coordinates against selected_sol to prevent LLM version/groupId mix-ups.\n",
            "    \"\"\"\n",
            "    results = []\n",
            "    print(\"=== APPLYING MIGRATION ACTIONS ON THE POM ===\")\n",
